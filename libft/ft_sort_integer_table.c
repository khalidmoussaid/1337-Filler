#include "libft.h"

void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < size)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
				ft_swap(tab + i, tab + j);
			j++;
		}
		i++;
	}
}
