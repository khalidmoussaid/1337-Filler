char	*ft_strcpy(char *dest, const char *src)
{
	int		i;
	char	*s;

	s = (char *)src;
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
